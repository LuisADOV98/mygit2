

function sumar(x,y,z) {
    let resultado = x+y+z
    return resultado
}

function restar(x,y=0) {
    let resultado = x-y
    return resultado
}

function producto(x,y=0) {
    let resultado = x*y
    return resultado
}

function division(x,y=0) {
    let resultado = x/y
    return resultado
}

function elevadoAl(n, n1) {

    let resultado = n ** n1
    return resultado
}

